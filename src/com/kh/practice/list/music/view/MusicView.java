package com.kh.practice.list.music.view;

import java.util.List;
import java.util.Scanner;

import com.kh.practice.list.music.controller.MusicController;
import com.kh.practice.list.music.model.vo.Music;

public class MusicView {

	private Scanner sc=new Scanner(System.in);
	private MusicController mc=new MusicController();
	
	public void mainMenu() {
		while(true) {
				System.out.println("******* 메인 메뉴 *******");
				System.out.println("1.마지막 위치에 곡 추가");
				System.out.println("2.첫 위치에 곡 추가");
				System.out.println("3.전체 곡 목록 출력");
				System.out.println("4.특정 곡 검색");
				System.out.println("5.특정 곡 삭제");
				System.out.println("6.특정 곡 정보 수정");
				System.out.println("7.곡명 오름차순 정렬");
				System.out.println("8.가수명 내림차순 정렬");
				System.out.println("9.종료"); 
				System.out.print("메뉴 번호 선택 :");
				int cho=sc.nextInt();
				
				switch(cho) {
					case 1 : addList();break;
					case 2 : addAtZero();break;
					case 3 : printAll();break;
					case 4 : searchMusic(); break;
					case 5 : removeMusic(); break;
					case 6 : setMusic(); break;
					case 7 : ascTitle(); break;
					case 8 : descSinger(); break;
					case 9 : System.out.println("종료"); return;
					default : System.out.println("잘못 입력하셨습니다.다시 입력해주세요.");
				}
				
		}
		
	}
	
	public void addList() {
		
		System.out.println("****** 마지막 위치에 곡 추가 ******");
		
		      sc.nextLine();
			System.out.print("곡 명:");
				String title=sc.nextLine();
			System.out.print("가수명:");
				String singer=sc.nextLine();
			Music m=new Music(title,singer);
		int success=mc.addList(m);
			if(success==1) {
					System.out.println("추가 성공");
			}else{System.out.println("추가 실패");}
		
	}
	
	public void addAtZero() {
		sc.nextLine();
		System.out.println("****** 첫 위치에 곡 추가 ******");
			System.out.print("곡 명:");
				String title=sc.nextLine();
			System.out.print("가수명 : ");
				String singer=sc.nextLine();
				Music m=new Music(title,singer);
			int succeess=mc.addAtZero(m);
		if(succeess==1){
			System.out.println("추가 성공");
		}else{System.out.println("추가 실패");}
		
	}
	
	public void printAll() {
		List list=mc.printAll();
		System.out.println("****** 전체 곡 목록 출력 ******");
		System.out.println(list);
//			for(int i=0;i<list.size();i++) {
//				System.out.println(list.get(i));
//			}
		
	}
	
    public void searchMusic() {
    	sc.nextLine();
    	System.out.println("****** 특정 곡 검색 ******");
    	System.out.print("검색할 곡 명:");
    	String title=sc.nextLine();
    	Music m=mc.searchMusic(title);
		if(m==null) {
			System.out.println("검색한 곡이 없습니다.");
		}else {
			System.out.println("검색한 곡은 "+m.getTitle()+","+m.getSinger()+"입니다.");
		}
	}
	
	public void removeMusic() {
		sc.nextLine();
		System.out.println("****** 특정 곡 삭제 ******");
		System.out.print("삭제할 곡 명:");
    	String title=sc.nextLine();
		Music m=mc.searchMusic(title);
    	if(m==null) {
    		System.out.println("삭제할 곡이 없습니다.");
    	}else {
    		mc.removeMusic(title);
    		System.out.println(m.getTitle()+", "+m.getSinger()+"을 삭제 했습니다.");
    	}
    	
	}
	
	public void setMusic() {
		sc.nextLine();
		System.out.println("****** 특정 곡 정보 수정 ******");
			System.out.print("수정할 곡 명:");
	    	String title=sc.nextLine();
			Music m=mc.searchMusic(title);
				if(m==null) {
					System.out.println("수정할 곡이 없습니다.");
				}else {
					mc.setMusic(m.getTitle(),m);
					System.out.println(m.getTitle()+", "+m.getSinger()+"의 값이 변경 되었습니다.");
					
				}
		
		
	}
	
	public void ascTitle() {
		sc.nextLine();
		System.out.println("****** 곡 명 오름차순 정렬 ******");
		int success=mc.ascTitle();
			if(success==1) {
				System.out.println("정렬 성공");
			}else {   System.out.println("정렬 실패");}
		
	}
	
	public void descSinger() {
		sc.nextLine();
		System.out.println("****** 가수 명 내림차순 정렬 ******");
			int success=mc.descSinger();
				if(success==1) {
					System.out.println("정렬 성공");
				} else {  System.out.println("정렬 실패");}
	}
	
}
